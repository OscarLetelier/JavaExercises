package fundamentos;

/*
Objetivo:
Imprimir numeros del 1 al 10
 */

public class N8_BucleFor {
    public static void main(String[] args) {
        System.out.println("Bienvenido al Iterador de Ciclo For");

        int i;

        for(i = 1; i <=10; i++){
            System.out.println("Iteracion de " +i);
        }
    }
}
