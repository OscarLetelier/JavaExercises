package Oscar.fundamentos;

public class VariablesTiposDeDatos {
    public static void main(String[] args) {

        // Tipos de DAtos
        int numero = 0;
        double numeroComa = 1.2;
        boolean con = true;
        boolean cons = false;
        char a = 'c';
        String nombre = "oscar";

        // Mostrar por consola los tipos de datos declarados
        System.out.println("Nombre : "+nombre);
        System.out.println("Numero Entero : "+numero);
        System.out.println("Numero con Coma : "+numeroComa);
        System.out.println("Valor verdadero : "+con);
        System.out.println("Valor Falso : "+cons);
        System.out.println("Caracter : "+a);
        System.out.println("String : "+ nombre);
    }
}
